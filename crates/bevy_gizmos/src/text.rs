//! Module that generates simple text layouts for text gizmos.

use std::ops::Range;

/// Simple stroke font
pub struct StrokeFont {
    pub advance: f32,
    pub points: Vec<Vec2>,
    pub strokes: Vec<Range<usize>>,
    /// A glyph consists of a number of strokes
    pub glyphs: Vec<Range<usize>>,
}

const ADVANCE: f32 = 0.6;
const LINEHEIGHT: f32 = 1.2;
const POSITIONS: [[f32; 2]; 9] = [
    [0., 1.], //0
    [0.5, 1.],
    [0.5, 0.],
    [0., 0.],
    [0., 1.],   // 4
    [0., 0.5],  // 5
    [0.5, 1.],  // 6
    [0., 1.],   // 7
    [0.5, 0.5], // 8
];
const STROKES: [Range<usize>; 3] = [0..5, 5..7, 7..9];
const GLYPHS: [Range<usize>; 2] = [0..1, 1..3];

use bevy_color::Color;
use bevy_math::{
    curve::{Curve, CurveExt},
    Isometry2d, Vec2, Vec3,
};

use crate::{gizmos::GizmoBuffer, prelude::GizmoConfigGroup};

impl<Config, Clear> GizmoBuffer<Config, Clear>
where
    Config: GizmoConfigGroup,
    Clear: 'static + Send + Sync,
{
    pub fn text_2d(
        &mut self,
        isometry: impl Into<Isometry2d>,
        text: &str,
        size: f32,
        line_height: f32,
        color: impl Into<Color>,
    ) {
        let isometry = isometry.into();
        let color = color.into();
        let advance = ADVANCE * size;
        let line_height = LINEHEIGHT * size;

        let mut rx = 0.;
        let mut ry = 0.;

        for c in text.chars() {
            if !c.is_ascii() {
                rx += advance;
                continue;
            }

            if c == '\n' {
                rx = 0.;
                ry -= line_height;
                continue;
            }

            if c == ' ' {
                rx += advance;
                continue;
            }

            if !c.is_ascii_alphanumeric() {
                rx += advance;
                continue;
            }

            let i = c.is_ascii_lowercase() as usize;

            let glyph = GLYPHS[i].clone();
            for stroke in &STROKES[glyph] {
                let positions = POSITIONS[stroke.clone()]
                    .iter()
                    .map(|&[x, y]| isometry * Vec2::new(rx + size * x, ry - size * y));
                self.linestrip_2d(positions, color);
            }
            rx += advance;
        }
    }

    pub fn text_2d_simplex(
        &mut self,
        isometry: impl Into<Isometry2d>,
        text: &str,
        size: f32,
        line_height: f32,
        color: impl Into<Color>,
    ) {
        let isometry = isometry.into();
        let color = color.into();
        let scale = size / SIMPLEX_CAP_HEIGHT;
        let line_height = line_height * size;

        let space_advance = SIMPLEX_FONT[1] as f32 * scale;
        let mut rx = 0.0;
        let mut ry = 0.0;

        for c in text.chars() {
            if c == '\n' {
                rx = 0.0;
                ry -= line_height;
                continue;
            }

            let code = c as u32;
            if !(SIMPLEX_ASCII_START..=SIMPLEX_ASCII_END).contains(&code) {
                rx += space_advance;
                continue;
            }

            let glyph_index = (code as usize - SIMPLEX_ASCII_START as usize) * SIMPLEX_GLYPH_STRIDE;
            let point_count = SIMPLEX_FONT[glyph_index] as usize;
            let advance = SIMPLEX_FONT[glyph_index + 1] as f32 * scale;
            let points = &SIMPLEX_FONT[glyph_index + 2..glyph_index + 2 + point_count * 2];

            let mut stroke = Vec::new();
            for pair in points.chunks_exact(2) {
                let x = pair[0];
                let y = pair[1];
                if x == -1 && y == -1 {
                    if stroke.len() >= 2 {
                        self.linestrip_2d(stroke.iter().copied(), color);
                    }
                    stroke.clear();
                    continue;
                }

                let position = isometry * Vec2::new(rx + scale * x as f32, ry - scale * y as f32);
                stroke.push(position);
            }

            if stroke.len() >= 2 {
                self.linestrip_2d(stroke.iter().copied(), color);
            }

            rx += advance;
        }
    }
}

